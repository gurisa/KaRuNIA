  function age_year($birthdate) {
    $res = "-";
    if (isset($birthdate) && !empty($birthdate)) {
      date_default_timezone_set('UTC');
      $data = explode("/", $birthdate);
      $year_diff  = date("Y") - $data[2];
      $month_diff = date("m") - $data[1];
      $day_diff   = date("d") - $data[0];

      if ($month_diff < 0) {
        $year_diff--;
      }
      elseif (($month_diff==0) && ($day_diff < 0))  {
        $year_diff--;
      }
      $res = $year_diff;
    }
    return $res;
  }

